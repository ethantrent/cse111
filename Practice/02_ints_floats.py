# Arithmetic
print(1 + 2) # add
print(1 - 2) # subtract
print(1 * 2) # multiply
print(1 / 2) # divide
print(1 // 2) # floor after division
print(1 ** 2) # exponent
print(1 % 2) # remainder after division
print(2 % 2)

# Comparisons
num1 = 1
num2 = 2

print(num1 == num2) # equal
print(num1 != num2) # not equal
print(num1 > num2) # greater than
print(num1 < num2) # less than
print(num1 >= num2) # greater than or equal to
print(num1 <= num2) # less than or equal to

